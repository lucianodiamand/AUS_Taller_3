
public class SoloDatos {

	// Visibilidad: private <package> protected public
	// campos
	// nivel de visibilidad | tipo | identificador | (opcional) inicializar
	private  int i;
	
	private char c;
	
	private double d;
	
	private boolean b;
	
	private String s;

	// metodos
	// nivel de visibilidad | tipo retorno | indentificador (tipo1 iden1, tipo2 ident2, ...) {
	
    // }

	public int getI() {
		return i;
	}

	public void setI(int i) {
		this.i = i;
	}

	public char getC() {
		return c;
	}

	public void setC(char c) {
		this.c = c;
	}

	public double getD() {
		return d;
	}

	public void setD(double d) {
		this.d = d;
	}

	public boolean isB() {
		return b;
	}

	public void setB(boolean b) {
		this.b = b;
	}

	public String getS() {
		return s;
	}

	public void setS(String s) {
		this.s = s;
	}

}
